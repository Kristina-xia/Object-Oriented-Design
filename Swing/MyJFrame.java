    public void setDay(JComboBox year,JComboBox month,JComboBox day)
    {
        String setDate=year.getSelectedItem().toString()+"-"+month.getSelectedItem().toString();
        Calendar cal=new GregorianCalendar();
        try
        {
            cal.setTime(new SimpleDateFormat("yyyy-MM").parse(setDate));
        }
        catch(ParseException ex)
        {
            Logger.getLogger(MyJFrame.class.getName()).log(Level.SEVERE,null,ex);
        }
 int dayNum=cal.getActualMaximum(Calendar.DAY_OF_MONTH);
        int dayDif=dayNum-day.getItemCount();
        if(dayDif>0)
        {
            for(int i=day.getItemCount()+1;i<=dayNum;i++)
            {
                day.addItem(i);
            }
        }
        else if(dayDif<0)
        {
            for(int i=day.getItemCount();i>dayNum;i--)
            {
                day.removeItem(i);
            }
        }
    }
    public void choosePic(JLabel jLabel1,JLabel jLabel2)
    {
        String picturePath=null;
        JFileChooser fd=new JFileChooser();
        fd.showOpenDialog(null);
        File fi=fd.getSelectedFile();
        if(fi!=null&&fi.getAbsolutePath().endsWith(".jpg"))
        {
            jLabel1.setText("");
            picturePath=fi.getAbsolutePath();
            jLabel1.setIcon(new ImageIcon(picturePath));
            jLabel2.setText(picturePath);
        }
    }

