    private void CreateBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtn1ActionPerformed
        Person person=new Person();
        person.setLastName(lastNameTextField.getText());
        person.setFirstName(firstNameTextField.getText());
        person.setNationality(nationalityTextField.getText());
        String sex=new String();
        if(maleBtn.isSelected())
        {
            sex="male";
        }
        else if(femaleBtn.isSelected())
        {
            sex="female";
        }
        person.setSex(sex);
        String marriedOrNot=new String();
        if(marriedBtn.isSelected())
        {
            marriedOrNot="married";
        }
        else if(notMarriedBtn.isSelected())
        {
            marriedOrNot="NotMarried";
        }
        person.setMarriedOrNot(marriedOrNot);
        
        LicenseData licenseData= new LicenseData();
        person.setLicenseData(licenseData);
        licenseData.setNumber(numberTextField.getText());
        licenseData.setDateofBirth(monthCb1.getSelectedItem().toString()+"/"+dateCb1.getSelectedItem().toString()+"/"+yearCb1.getSelectedItem().toString());
        licenseData.setExp(monthCb2.getSelectedItem().toString()+"/"+yearCb2.getSelectedItem().toString());
        licenseData.setRest(restCb.getSelectedItem().toString());

        if(maleBtn1.isSelected())
        {
            sex="male";
        }
        else if(femaleBtn1.isSelected())
        {
            sex="female";
        }
        person.getLicenseData().setSex(sex);

        
        CreditCard creditCard=new CreditCard();
        person.setCreditCard(creditCard);
        creditCard.setCurrentBalance(currentBalanceTextField.getText());
        creditCard.setCredit(new Float(creditTextField.getText()));
        creditCard.setCardNum(cardNumTextField.getText());
        creditCard.setValidThru(monthCb3.getSelectedItem().toString()+"/"+yearCb3.getSelectedItem().toString());
        creditCard.setSince(yearCb4.getSelectedItem().toString());
        
        Address address=new Address();
        person.setAddress(address);
        address.setStreetLine(streetLineTextField.getText());
        address.setUnit(unitTextField.getText());
        address.setCity(cityTextField.getText());
        address.setZipCode(zipCodeTextField.getText());
        address.setState(stateTextField.getText());
        
        
        FinancialAccounts financialAccounts=new FinancialAccounts();
        person.setFinanicalAccounts(financialAccounts);
        CheckingAccounts checkingAccounts=new CheckingAccounts();
        financialAccounts.setCheckingAccounts(checkingAccounts);
        checkingAccounts.setDebt(debtTextField1.getText());
        checkingAccounts.setBalance(balanceTextField1.getText());
        checkingAccounts.setCredit(creditTextField1.getText());
        String activeOrNotActive=new String();
        if(activeBtn1.isSelected())
        {
            activeOrNotActive="active";
        }
        else if(notActiveBtn1.isSelected())
        {
            activeOrNotActive="NotActive";
        }
        checkingAccounts.setActiveOrNotActive(activeOrNotActive);
        checkingAccounts.setCreationDate(monthCb5.getSelectedItem().toString()+"/"+dateCb5.getSelectedItem().toString()+"/"+yearCb5.getSelectedItem().toString());
        
        SavingAccounts savingAccounts=new SavingAccounts();
        financialAccounts.setSavingAccounts(savingAccounts);
        savingAccounts.setDebt(debtTextField4.getText());
        savingAccounts.setBalance(balanceTextField4.getText());
        savingAccounts.setCredit(creditTextField4.getText());
        if(activeBtn4.isSelected())
        {
            activeOrNotActive="active";
        }
        else if(notActiveBtn4.isSelected())
        {
            activeOrNotActive="NotActive";
        }
        savingAccounts.setActiveOrNotActive(activeOrNotActive);
        savingAccounts.setCreationDate(monthCb6.getSelectedItem().toString()+"/"+dateCb6.getSelectedItem().toString()+"/"+yearCb6.getSelectedItem().toString());
        
        person.setPicturePath(uploadLabel.getText());
        
        Person.list.add(person);
        
        
  
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, "Create Product Successfully");
        // TODO add your handling code here:
    }//GEN-LAST:event_CreateBtn1ActionPerformed

